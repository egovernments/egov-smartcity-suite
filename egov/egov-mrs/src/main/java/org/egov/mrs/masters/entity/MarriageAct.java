package org.egov.mrs.masters.entity;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;

import org.egov.infra.persistence.entity.AbstractAuditable;
import org.hibernate.validator.constraints.Length;
import org.hibernate.validator.constraints.SafeHtml;

@Entity
@Table(name = "egmrs_act")
@SequenceGenerator(name = MarriageAct.SEQ_ACT, sequenceName = MarriageAct.SEQ_ACT, allocationSize = 1)
public class MarriageAct extends AbstractAuditable {

    private static final long serialVersionUID = -8796522085867045434L;
    public static final String SEQ_ACT = "SEQ_EGMRS_ACT";

    @Id
    @GeneratedValue(generator = SEQ_ACT, strategy = GenerationType.SEQUENCE)
    private Long id;

    @SafeHtml
    @NotNull
    @Length(max = 30)
    private String name;

    @SafeHtml
    @Length(max = 50)
    private String description;

    @Override
    public Long getId() {
        return id;
    }

    @Override
    public void setId(final Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(final String description) {
        this.description = description;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = super.hashCode();
        result = prime * result + (id == null ? 0 : id.hashCode());
        result = prime * result + (name == null ? 0 : name.hashCode());
        return result;
    }

    @Override
    public boolean equals(final Object obj) {
        if (this == obj)
            return true;

        if (!super.equals(obj))
            return false;

        if (getClass() != obj.getClass())
            return false;

        final MarriageAct other = (MarriageAct) obj;

        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;

        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;

        return true;
    }

}
